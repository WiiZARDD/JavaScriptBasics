/*
Different ways to use strings
1: ''
2: ""
3: ``
*/

console.log('Hello World!');
console.log("Hello World!");
console.log(`Hello World!`);

//Split point
console.log(` `);

/*Different ways to log string outputs
.toUpperCase (MAKES ALL CAPS)
.toLowerCase (makes all lowercase)
.trim (Removes      Any         Tab         Space)
*/

console.log('Hello World'.toUpperCase());
console.log('Hello World'.toLowerCase());
console.log('   Hello World    '.trim());

//Split point
console.log(` `);

//Different ways to use string types

console.log('Hello "World"'.toUpperCase());
console.log("Hello World".toLowerCase());
console.log(`   Hello's "World"\nNew Line!    `.trim());
